    function validateLessonPayload(payload) {
      if (!payload.title || !payload.theory || !payload.formulas.length || !payload.example || !payload.task.question || !payload.task.correctAnswer) {
        throw new Error("Р—Р°РїРѕР»РЅРё РІСЃРµ РїРѕР»СЏ СѓСЂРѕРєР°.");
      }
      if (payload.task.type === "text") {
        delete payload.task.options;
      } else {
        if (!payload.task.options || payload.task.options.length < 3) throw new Error("Р”Р»СЏ single Рё multiple РЅСѓР¶РЅРѕ РјРёРЅРёРјСѓРј 3 РІР°СЂРёР°РЅС‚Р° РѕС‚РІРµС‚Р°.");
        if (payload.task.type === "single" && !payload.task.options.includes(payload.task.correctAnswer)) {
          throw new Error("РџСЂР°РІРёР»СЊРЅС‹Р№ РѕС‚РІРµС‚ РґРѕР»Р¶РµРЅ СЃРѕРІРїР°РґР°С‚СЊ СЃ РѕРґРЅРёРј РёР· РІР°СЂРёР°РЅС‚РѕРІ.");
        }
        if (payload.task.type === "multiple") {
          if (!Array.isArray(payload.task.correctAnswer) || !payload.task.correctAnswer.length) throw new Error("Р”Р»СЏ multiple СѓРєР°Р¶Рё С…РѕС‚СЏ Р±С‹ РѕРґРёРЅ РїСЂР°РІРёР»СЊРЅС‹Р№ РѕС‚РІРµС‚.");
          if (!payload.task.correctAnswer.every((answer) => payload.task.options.includes(answer))) throw new Error("Р’СЃРµ РїСЂР°РІРёР»СЊРЅС‹Рµ РѕС‚РІРµС‚С‹ РґРѕР»Р¶РЅС‹ РїСЂРёСЃСѓС‚СЃС‚РІРѕРІР°С‚СЊ СЃСЂРµРґРё РІР°СЂРёР°РЅС‚РѕРІ.");
        }
      }
    }

    function saveModalForm(form) {
      const formData = new FormData(form);
      if (state.modal.type === "course") {
        const title = String(formData.get("title") || "").trim();
        const description = String(formData.get("description") || "").trim();
        if (!title || !description) return showToast("Р—Р°РїРѕР»РЅРё РЅР°Р·РІР°РЅРёРµ Рё РѕРїРёСЃР°РЅРёРµ РєСѓСЂСЃР°.");
        if (state.modal.mode === "create") {
          appData.courses.unshift({ id: uid("course", title), title, description, modules: [] });
          showToast("РљСѓСЂСЃ СЃРѕР·РґР°РЅ.");
        } else {
          const course = getCourseById(state.modal.courseId);
          if (!course) return;
          course.title = title;
          course.description = description;
          showToast("РљСѓСЂСЃ РѕР±РЅРѕРІР»С‘РЅ.");
        }
      }
      if (state.modal.type === "module") {
        const title = String(formData.get("title") || "").trim();
        if (!title) return showToast("Р’РІРµРґРёС‚Рµ РЅР°Р·РІР°РЅРёРµ РјРѕРґСѓР»СЏ.");
        const course = getCourseById(state.modal.courseId);
        if (!course) return;
        if (state.modal.mode === "create") {
          course.modules.push({ id: uid("module", title), title, lessons: [] });
          showToast("РњРѕРґСѓР»СЊ РґРѕР±Р°РІР»РµРЅ.");
        } else {
          const module = getModuleById(course, state.modal.moduleId);
          if (!module) return;
          module.title = title;
          showToast("РњРѕРґСѓР»СЊ РѕР±РЅРѕРІР»С‘РЅ.");
        }
      }
      if (state.modal.type === "lesson") {
        const taskType = formData.get("taskType");
        const optionsText = String(formData.get("options") || "").trim();
        const correctRaw = String(formData.get("correctAnswer") || "").trim();
        const payload = {
          id: state.modal.lessonId || uid("lesson", formData.get("title")),
          title: String(formData.get("title") || "").trim(),
          theory: String(formData.get("theory") || "").trim(),
          formulas: String(formData.get("formulas") || "").split("\n").map((item) => item.trim()).filter(Boolean),
          example: String(formData.get("example") || "").trim(),
          task: {
            type: taskType,
            question: String(formData.get("question") || "").trim(),
            correctAnswer: taskType === "multiple" ? correctRaw.split("\n").map((item) => item.trim()).filter(Boolean) : correctRaw
          }
        };
        if (taskType !== "text") payload.task.options = optionsText.split("\n").map((item) => item.trim()).filter(Boolean);
        try {
          validateLessonPayload(payload);
        } catch (error) {
          return showToast(error.message);
        }
        const course = getCourseById(state.modal.courseId);
        const module = getModuleById(course, state.modal.moduleId);
        if (!course || !module) return;
        if (state.modal.mode === "create") {
          module.lessons.push(payload);
          showToast("РЈСЂРѕРє РґРѕР±Р°РІР»РµРЅ.");
        } else {
          const index = module.lessons.findIndex((lesson) => lesson.id === state.modal.lessonId);
          if (index === -1) return;
          module.lessons[index] = payload;
          showToast("РЈСЂРѕРє РѕР±РЅРѕРІР»С‘РЅ.");
        }
      }
      saveData();
      closeModal();
      renderApp();
      renderSidebar();
    }

